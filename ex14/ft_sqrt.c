int	ft_sqrt(int nb)
{
	int	c;

	c = 1;
	if (nb > 0)
	{
		while ((c * c) <= nb)
		{
			if (c * c == nb)
				return (c);
			else if (c >= 46341)
				return (0);
			c++;
		}
	}
	return (0);
}
